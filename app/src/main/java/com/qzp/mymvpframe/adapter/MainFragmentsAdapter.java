package com.qzp.mymvpframe.adapter;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentPagerAdapter;

import java.util.List;

/**
 * Created by qzp on 2018/11/30.
 */

public class MainFragmentsAdapter extends FragmentPagerAdapter{


    private List<Fragment> mListFragments = null;

    public MainFragmentsAdapter(FragmentManager fm, List<Fragment> fragments) {
        super(fm);
        mListFragments = fragments;
    }

    @Override
    public int getCount() {
        return null != mListFragments ? mListFragments.size() : 0;
    }

    @Override
    public Fragment getItem(int index) {
        if (mListFragments != null && index > -1 && index < mListFragments.size()) {
            return mListFragments.get(index);
        } else {
            return null;
        }
    }

}
